package com.nhi;

public class Basics {
    public static void main(String[] args) {
        int a = 10;
        if (a == 10) {
            System.out.println("Hello world");
        }

        // while loop
        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }

        // for loop
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
    }
}
